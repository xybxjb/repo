package cn.deepcoding.model;

public class StudentProxyTeacher {

	private Integer id;
	private String studentName;
	private String studentPhone;
	private String proxyTeacherName;
	private String proxyTeacherPhone;
	private String remark;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getStudentPhone() {
		return studentPhone;
	}
	public void setStudentPhone(String studentPhone) {
		this.studentPhone = studentPhone;
	}
	public String getProxyTeacherName() {
		return proxyTeacherName;
	}
	public void setProxyTeacherName(String proxyTeacherName) {
		this.proxyTeacherName = proxyTeacherName;
	}
	public String getProxyTeacherPhone() {
		return proxyTeacherPhone;
	}
	public void setProxyTeacherPhone(String proxyTeacherPhone) {
		this.proxyTeacherPhone = proxyTeacherPhone;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
